function addNewStaff (){
    addModal.classList.add("scale-100")
    addModal.classList.add("duration-200")
    addModal.classList.remove("scale-0");

    let img = addForm.querySelector(".add-img");
    let image = null;

    let addInput = addForm.querySelector(".add-input");
    addInput.addEventListener("change", (e)=>{
        img.src = URL.createObjectURL(e.target.files[0]);
        image = URL.createObjectURL(e.target.files[0])
    });

    addButtonNYes.addEventListener("click", ()=>{
        let newUser = {
            name: {
                first : addForm[0].value,
                last : addForm[1].value
            },
            email : addForm[2].value,
            phone : addForm[3].value,
            location: {
                city : addForm[4].value
            },
            picture: {
                large : image
            }
        }
        
        results.push(newUser);
        renderTable(results);

        addModal.classList.add("scale-0")
        addModal.classList.remove("scale-100")
    })
}

