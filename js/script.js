function renderTable (arr){
    tablebody.innerHTML = "";
    
    if (arr.length < 1) {
        tablebody.innerHTML = `<p class="text-center mt-5">No user is found</p>`
    }
    else if (arr.length > 0){
        const allPeople = arr.map((person, index) => {
            return (`
                <tr class="text-center" key="${index}">
                    <td class="text-left">${index + 1}</td>
                    <td>${person.name.first}</td>
                    <td>${person.name.last}</td>
                    <td>${person.email}</td>
                    <td>${person.phone}</td>
                    <td class="flex items-center justify-center">
                        <img src="${person.picture.large}" alt="${person.name.first}" width="60" height="60"/>
                    </td>
                    <td>${person.location.city}</td>    
                    <td>
                        <button onclick="deleteUser(${person.id})">
                            <img src="./images/delete.svg" alt="Delete" width="30" height="30"/>
                        </button>
                    </td>
                    <td>
                        <button onclick="updateUser(${person.id})">
                            <img src="./images/update.svg" alt="Delete" width="30" height="30"/>
                        </button>
                    </td>
                </tr>
            `
            )
        })
        tablebody.innerHTML = allPeople.join("")
    }
    localStorage.setItem("results", JSON.stringify(results))
}
